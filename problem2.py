 
num1=int(input("Enter 1st number:"))
num2=int(input("Enter 2nd number:"))

op=input("Opration (+,-,*,/):")
if op=="+":
 print("result:",num1+num2)
elif op=="-":
 print("result:",num1-num2)
 
elif op=="*":
 print("result:",num1*num2)
elif op=="/":
 print("result:",num1/num2)
else:
 print("Invalid")
